# Conditional Statements:
# simple if : if a>b : print("greater value)
# if else : if a>b: print("greater value) ;  else : print("lesser value")
a = 30
b = 30

if a>b:
    print("greater value")
elif a<b:
    print("lesser value")
elif a==b: # double == means comparing and single = means assigning
    print("equals")
else:
    print("not valid")
